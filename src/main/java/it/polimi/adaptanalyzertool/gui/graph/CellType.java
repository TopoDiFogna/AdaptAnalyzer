package it.polimi.adaptanalyzertool.gui.graph;

/**
 * <p>This enum contains all the supported cell types.</p>
 *
 * @author Paolo Paterna
 * @version 0.1
 */
public enum CellType {

    /**
     * A rectangle filled with a solid color.
     */
    RECTANGLE,
    /**
     * An equilateral triangle filled with a solid color.
     */
    TRIANGLE,
    /**
     * A circle filled with a solid color.
     */
    CIRCLE

}
